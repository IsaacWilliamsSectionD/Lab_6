// Working with Variables
let myNumber = 10;
let myString = "Hello, JavaScript!";

// Print variables to the console
console.log(myNumber);  // Output: 10
console.log(myString);  // Output: Hello, JavaScript!

// Loop from 0 to 10 and print the numbers
for (let i = 0; i <= 10; i++) {
    console.log(i);  // Output: 0, 1, 2, ..., 10
}

// Print the first 5 multiples of 3
for (let i = 1; i <= 5; i++) {
    console.log(3 * i);  // Output: 3, 6, 9, 12, 15
}

// Function to add two numbers
function addNumbers(num1, num2) {
    return num1 + num2;
}

// Call the function and print the result
console.log(addNumbers(5, 7));  // Output: 12

// Function to multiply two numbers
function multiplyNumbers(num1, num2) {
    return num1 * num2;
}

// Call the function and print the result
console.log(multiplyNumbers(4, 8));  // Output: 32
